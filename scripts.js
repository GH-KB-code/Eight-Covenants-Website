let currentIndex = 0;

function renderCovenantList() {
    const listContainer = document.getElementById('covenantList');
    listContainer.innerHTML = ''; // Clear the current content

    covenantData.forEach((covenant, index) => {
        const li = document.createElement('li');
        li.classList.add('covenant-item');
        li.setAttribute('onclick', `showCovenantDetails(${index})`);

        const div = document.createElement('div');
        div.classList.add('circle');
        const img = document.createElement('img');
        img.src = covenant.imgSrc;
        img.alt = covenant.title;
        div.appendChild(img);

        const h3 = document.createElement('h3');
        h3.textContent = covenant.title;

        li.appendChild(div);
        li.appendChild(h3);
        listContainer.appendChild(li);
    });
}



function showCovenantDetails(index) {
    const detailsSection = document.getElementById('details');
    const detailsDiv = document.getElementById('covenantDetails');
    const covenant = covenantData[index];

    detailsDiv.innerHTML = `
        <h3>${covenant.title}</h3>
        <p>${covenant.description}</p>
        ${covenant.audioSrc ? `<button class="audio-button" onclick="playAudio(${index})">Click to Hear My Voice</button>` : ''}
        <audio id="audio${index}" src="${covenant.audioSrc}"></audio>
    `;
    detailsSection.classList.remove('hidden');
}

function playAudio(index) {
    const audio = document.getElementById(`audio${index}`);
    audio.play();
}

function closeDetails() {
    document.getElementById('details').classList.add('hidden');
}

renderCovenantList();
